public class FinalConsept {  // Final is a keyword

    public static void main(String[] args) {
         int i = 10;
         i = 20;
         i = 30;
        System.out.println(i); // 30

        final int j = 10; // constant values
      //  j = 20;
      //  j = 30;
        System.out.println(j); // 10

    }
}
